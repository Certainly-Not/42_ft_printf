#include "ft_printf.h"

void	ft_putstr_fd(char *s, int fd)
{
	if (!s)
	{
		write(1, "(null)", 6);
		return ;
	}
	while (*s)
	{
		ft_putchar_fd(s[0], fd);
		s++;
	}
}
